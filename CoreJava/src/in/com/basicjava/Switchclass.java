package in.com.basicjava;

public class Switchclass {
	public static void main(String[]args) {
		int value=1;
		switch (value) {
		case 1:
			System.out.println("Rishendra");
	
			break;
		case 2:
			System.out.println("Dwivedi");
            break;
            
		default:
			System.out.println("not found");
			break;
		}
		
	}
}
