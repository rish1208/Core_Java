package im.com.OOP;

public class Account {
	private String name;
	private int id;
	private int  amount;
	private int withdrawal;
	
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setID() {
		this.id=id;
	}
	public int getID() {
		return id;
	}
	
	public void setWithdrawal(int withdrawal) {
		this.withdrawal = withdrawal;
	}
	public int getWithdrawal() {
		return withdrawal;
	}
}
	
